							<h3>How is transitioning Ryan White consumers to Birthday and Half Birthday eligibility renewals beneficial?</h3>
							<p>Everyone has many dates to keep in mind.  Consumers of Ryan White services have a particularly important date to remember:  every six months, they must be certified as eligible to receive Ryan White funded services.  Without this eligibility determination, they cannot receive services such as medication and medical care paid for by the Ryan White Care Act.  We want to share our experience on how changing the eligibility renewal dates to something the consumers, case managers and providers can easily remember had a positive impact. </p>
							<p>Our client, the AIDS Drug Assistance Program (ADAP) in Arizona, wanted to serve consumers better with their eligibility renewal.  There were two main issues. First, consumers enrolled in ADAP in Arizona seemed to have trouble remembering when it was time to renew their eligibility for services.  Even with letters mailed out 45 days in advance, consumers continued to fall out of care due to failure to complete the eligibility renewal process.  Additionally, there were certain times of the year that case managers were swamped with renewals and other times when renewals were few and far between. TriYoung’s clients needed a way to help the people they serve remember when to get their eligibility renewed and at the same time equalize the workflow for workers processing eligibility month to month.  It was time to think outside the box and turn ideas into products.  TriYoung met with our clients and brainstormed.  During this process, an idea for a birthday month renewal was suggested. To fulfill the requirement of an eligibility update every 6 months, the half birthday idea was developed as well.  It was something simple that everyone remembers, tied to eligibility.</p>
							<p>In 2014, after presenting the idea to their HRSA project officer and securing buy-in, ADAP transitioned ADAP renewal months to the consumer’s birthday and half birthday month. TriYoung was tasked with developing a unique eligibility tool that integrates with CAREWare to calculate consumers’ eligibility renewals and generate notifications. We worked with Arizona ADAP to define the upcoming renewal type and notice dates. We then worked together to determine what indicates whether a full application is required. </p>
							<p>One of the first tasks we had to accomplish was defining what indicates that the consumer is due for a Birthday, Half Birthday or Full/Birthday enrollment. We worked with the ADAP Program Manager to determine what documents would be required for each of the different renewal types and developed the following:</p>
							<ul>
								<li><strong>Half Birthday</strong> : The consumer is due for an attestation only. No documents are needed unless there are changes. </li>
								<li><strong>Birthday</strong> : The consumer must complete a full application and provide all the required supporting documents.</li>
								<li><strong>Birthday</strong> : The consumer must complete a full application and provide all the required supporting documents.</li>
							</ul>
							<p>We then had to define how consumers who are new to Ryan White and did not apply for services during their birthday or half birthday months would be handled. Our solution was that whenever consumers establish eligibility initially, i.e., new to Ryan White, they would complete the full application, and their renewal date would be their upcoming birthday or half birthday as the renewal date. This date is automatically calculated based upon the current system date and the consumer’s birthday.  With this change, new consumers may be required to go through an eligibility process an additional time, depending on the time of year they come in as it relates to their birthday.  </p>
							<p>Another question that arose is whether each consumer needed to renew on the actual, exact day of the month they were born. We determined it would be best to not use the actual day of the month but to use the last day of the month that relates to the Birthday or Half Birthday. Here are some examples:</p>
							<p><strong>Joe Consumer</strong> : New to RW services – Birthday is in July</p>
							<ul>
								<li>Due for renewal by 5/31/18</li>
								<li>Joe comes in May to renew eligibility.  The process completes, and Joe will next renew next by 11/30/18</li>
							</ul>
							<p><strong>Mary Consumer</strong>  : New to RW services – Birthday is in July</p>
							<ul>
								<li>On 5/15/18, Mary has an initial intake and is now eligible until her birthday and is set to renew no later than 7/31/18</li>
								<li>For 2018, Mary will have to renew again in 60 days to keep aligned with her birthday/half birthday</li>
							</ul>
							<p><strong>Bill Consumer</strong> : Birthday is in January</p>
							<ul>
								<li>Bill last completed eligibility in January Currently, Bill needs to renew by 07/31/18 – Half Birthday</li>
							</ul>
							<p>Transitioning eligibility for Ryan White consumers to birthday and half birthday renewals is extremely beneficial to more than just the consumers. Changing the renewal dates made it easier for consumers, case managers and providers to know when consumers need to renew their Ryan White eligibility. It also helped even out the monthly workload for the eligibility staff. Prior to this change, June and December had the largest number of application renewals which required longer hours and extra help for processing applications. Now, the renewals are evenly distributed throughout the year and retention in care has improved. Most consumers now remember when they are due for a renewal. Case managers can now run reports of consumers due for renewal each month and proactively contact them. Initially, transitioning consumer eligibility to birthday and half birthday renewals seemed like a huge undertaking but working collaboratively, we defined the parameters making it manageable and more beneficial than initially predicted. </p>
